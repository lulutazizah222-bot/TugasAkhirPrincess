# operator aritmatika
a = 10
b = 3

print(a + b)   # Penjumlahan
print(a - b)   # Pengurangan
print(a * b)   # Perkalian
print(a / b)   # Pembagian (hasil float)
print(a // b)  # Pembagian bulat
print(a % b)   # Sisa pembagian
print(a ** b)  # Pangkat

# operator assignment

x = 10      # assignment biasa
x += 5      # sama dengan x = x + 5
x -= 2      # sama dengan x = x - 2
x *= 3      # sama dengan x = x * 3
x /= 2      # sama dengan x = x / 2

# operator perbandingan 

a = 10
b = 20

print(a == b)  # Sama dengan
print(a != b)  # Tidak sama
print(a > b)   # Lebih besar
print(a < b)   # Lebih kecil
print(a >= b)  # Lebih besar sama
print(a <= b)  # Lebih kecil sama

# operator logika

x = 5

print(x > 2 and x < 10)  # True (dua-duanya benar)
print(x > 10 or x < 10)  # True (salah satu benar)
print(not(x > 10))       # True (kebalikannya)

#  string

text = "Hello World"

print(text.upper())    # Ubah ke huruf besar
print(text.lower())    # Ubah ke huruf kecil
print(len(text))       # Hitung panjang string
print("Hello" in text) # Cek apakah "Hello" ada di string

